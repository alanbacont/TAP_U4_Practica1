            Statement s = conexion.createStatement();

            String SQL = "UPDATE `practica_1`.`productos` SET `DESCRIPCION` = '%des',`PRECIO` = '%prec',`EXISTENCIA` = '%exis' WHERE IDPRODUCTOS = " + id;

            SQL = SQL.replace("%des", jTextField1.getText());
