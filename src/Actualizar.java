    private void obtener(String id){
        try{
            DriverManager.registerDriver(new org.gjt.mm.mysql.Driver());
            
            Connection conexion = DriverManager.getConnection("jdbc:mysql://127.0.0.1:3306/?autoReconnect=true&useSSL=false","username","diegojuan569");
            
            Statement transaccion = conexion.createStatement();
            
            String SQL = "SELECT * FROM practica_1.productos WHERE IDPRODUCTOS = "+id;
            
            ResultSet resultado = transaccion.executeQuery(SQL);
            
            if(resultado.next()){
                //Si next es true significa que si encontro
                jTextField1.setText(resultado.getString(2));
                jTextField2.setText(resultado.getString(3));
                jTextField3.setText(resultado.getString(4));
            }else{
                mensaje("SE BORRO CON EXITO ID "+id);
            }
            conexion.close();
            
            
        }catch(Exception e){
            mensaje(e.getMessage());
        }
        
    }
    
